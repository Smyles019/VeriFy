import mongoose from 'mongoose';

const readerApplicationSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ['pending', 'approved', 'rejected'],
    default: 'pending',
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  document: {
  type: String, // Store the filename or full path
},
});

export default mongoose.model('ReaderApplication', readerApplicationSchema);
